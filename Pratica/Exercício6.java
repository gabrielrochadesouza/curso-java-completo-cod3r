package Pratica1;

public class Exercício6 {

	public static void main(String[] args) {
		
	// Símbolos +(mais), -(menos), *(vezes), /(divisão)
	
	var x = 20.60;
	double y = 15.50;
	
	System.out.println(x + y);
	System.out.println(x - y);
	System.out.println(x * y);
	System.out.println(x / y);
	
	int a = 9;
	int b = 15;
	
	System.out.println(a + b);
	System.out.println(a - b);
	System.out.println(a * b);
	System.out.println(a / b);
	
	System.out.println(a % b);
	System.out.println(19 % 5);
	
	System.out.println(x + y - a * b);
	}
}
